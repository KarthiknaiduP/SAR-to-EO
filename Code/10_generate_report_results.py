"""Generate report-ready tables, plots, Markdown, and LaTeX from real outputs.

This script never invents missing metrics. Run 08 and 09 first.

Kaggle:
    python 10_generate_report_results.py
"""

from __future__ import annotations

import argparse
import csv
import json
import math
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np

from sar2eo_common import DEFAULT_OUT


ARCHITECTURES = {
    "model_1_unet_l1": ("U-Net", "L1", "Paired deterministic baseline"),
    "model_2_pix2pix": ("U-Net + PatchGAN", "BCE GAN + L1", "Paired adversarial baseline"),
    "model_3_pix2pix_perceptual": (
        "U-Net + PatchGAN",
        "BCE GAN + L1 + perceptual + feature matching",
        "Feature-constrained paired GAN",
    ),
    "model_4_attention_resunet": (
        "Attention ResUNet + spectral PatchGAN",
        "Hinge GAN + L1 + perceptual + feature matching",
        "Improved paired GAN",
    ),
    "model_5_final_fusion_gan": (
        "Warm-started Attention ResUNet + spectral PatchGAN",
        "Hinge GAN + L1 + perceptual + feature matching + gradient + EMA",
        "Final evidence-driven paired model",
    ),
}


def arguments() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--out_dir", default=DEFAULT_OUT)
    parser.add_argument("--split", default="test", choices=["val", "test"])
    parser.add_argument("--report_dir", default=None)
    return parser.parse_args()


def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open() as handle:
        return list(csv.DictReader(handle))


def value(row: dict[str, str], key: str) -> float:
    try:
        return float(row[key])
    except (KeyError, TypeError, ValueError):
        return float("nan")


def display(number: float, digits: int = 4) -> str:
    return "N/A" if not math.isfinite(number) else f"{number:.{digits}f}"


def latex_escape(text: str) -> str:
    return (
        text.replace("\\", r"\textbackslash{}")
        .replace("_", r"\_")
        .replace("&", r"\&")
        .replace("%", r"\%")
        .replace("#", r"\#")
    )


def collect_training(out_dir: Path) -> list[dict]:
    rows = []
    for run_name in ARCHITECTURES:
        losses_path = out_dir / run_name / "losses.csv"
        if not losses_path.exists():
            continue
        history = read_csv(losses_path)
        best = min(history, key=lambda row: value(row, "val_l1"))
        rows.append(
            {
                "model": run_name,
                "epochs_completed": len(history),
                "best_epoch": int(float(best["epoch"])),
                "best_val_l1": value(best, "val_l1"),
                "training_minutes": sum(value(row, "seconds") for row in history) / 60,
            }
        )
    return rows


def plot_training_curves(out_dir: Path, path: Path) -> None:
    figure, axis = plt.subplots(figsize=(10, 6))
    plotted = False
    for run_name in ARCHITECTURES:
        losses_path = out_dir / run_name / "losses.csv"
        if not losses_path.exists():
            continue
        rows = read_csv(losses_path)
        axis.plot(
            [value(row, "epoch") for row in rows],
            [value(row, "val_l1") for row in rows],
            label=run_name.replace("model_", "M").replace("_", " "),
            linewidth=2,
        )
        plotted = True
    if not plotted:
        plt.close(figure)
        return
    axis.set(xlabel="Epoch", ylabel="Validation L1", title="Validation reconstruction error")
    axis.grid(alpha=0.25)
    axis.legend(fontsize=8)
    figure.tight_layout()
    figure.savefig(path, dpi=200)
    plt.close(figure)


def winners(results: list[dict[str, str]]) -> dict[str, str]:
    output = {}
    for metric, maximize in [("psnr", True), ("ssim", True), ("mae", False), ("lpips", False), ("fid", False)]:
        valid = [row for row in results if math.isfinite(value(row, metric))]
        if valid:
            winner = (max if maximize else min)(valid, key=lambda row: value(row, metric))
            output[metric] = winner["model"]
    return output


def write_latex(results: list[dict[str, str]], training: list[dict], path: Path) -> None:
    lines = [
        "% Auto-generated by 10_generate_report_results.py",
        r"\begin{table*}[t]",
        r"\centering",
        r"\caption{SAR-to-EO test-set comparison. Arrows indicate the preferred direction.}",
        r"\label{tab:model-comparison}",
        r"\begin{tabular}{lrrrrrr}",
        r"\toprule",
        r"Model & PSNR $\uparrow$ & SSIM $\uparrow$ & MAE $\downarrow$ & LPIPS $\downarrow$ & FID $\downarrow$ & ms/image $\downarrow$ \\",
        r"\midrule",
    ]
    for row in results:
        lines.append(
            f"{latex_escape(row['model'])} & {display(value(row, 'psnr'), 3)} & "
            f"{display(value(row, 'ssim'), 4)} & {display(value(row, 'mae'), 4)} & "
            f"{display(value(row, 'lpips'), 4)} & {display(value(row, 'fid'), 2)} & "
            f"{display(value(row, 'milliseconds_per_image'), 2)} \\\\"
        )
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table*}", ""]
    if training:
        lines += [
            r"\begin{table}[t]",
            r"\centering",
            r"\caption{Training budget and checkpoint selection.}",
            r"\label{tab:training-budget}",
            r"\begin{tabular}{lrrrr}",
            r"\toprule",
            r"Model & Epochs & Best epoch & Val. L1 & Minutes \\",
            r"\midrule",
        ]
        for row in training:
            lines.append(
                f"{latex_escape(row['model'])} & {row['epochs_completed']} & {row['best_epoch']} & "
                f"{display(row['best_val_l1'], 4)} & {display(row['training_minutes'], 1)} \\\\"
            )
        lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    path.write_text("\n".join(lines))


def write_markdown(
    results: list[dict[str, str]],
    terrain: list[dict[str, str]],
    training: list[dict],
    eda: dict,
    failure: list[dict],
    report_dir: Path,
) -> None:
    win = winners(results)
    lines = [
        "# SAR-to-EO Progressive Model Study",
        "",
        "## Experimental Design",
        "",
        "The study progresses from a deterministic paired baseline to adversarial, feature-constrained, attention-based, and final refined paired translation. All reported models use the same scene-disjoint manifest and test samples.",
        "",
        "| Stage | Architecture | Objective | Research role |",
        "|---|---|---|---|",
    ]
    for index, (name, (architecture, objective, role)) in enumerate(ARCHITECTURES.items(), 1):
        lines.append(f"| M{index}: {name} | {architecture} | {objective} | {role} |")
    lines += ["", "## Dataset", ""]
    if eda:
        lines += [
            f"- Total paired patches: {eda.get('total_pairs', 'N/A')}",
            f"- Unique scenes: {eda.get('unique_scenes', 'N/A')}",
            f"- Scene-disjoint split counts: {eda.get('split_counts', 'N/A')}",
            f"- Terrain counts: {eda.get('terrain_counts', 'N/A')}",
        ]
    else:
        lines.append("EDA statistics were not found; run Script 01 before finalizing the report.")
    lines += [
        "",
        "## Test Results",
        "",
        "| Model | PSNR ↑ | SSIM ↑ | MAE ↓ | LPIPS ↓ | FID ↓ | M parameters | ms/image |",
        "|---|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for row in results:
        lines.append(
            f"| {row['model']} | {display(value(row, 'psnr'), 3)} | {display(value(row, 'ssim'))} | "
            f"{display(value(row, 'mae'))} | {display(value(row, 'lpips'))} | {display(value(row, 'fid'), 2)} | "
            f"{display(value(row, 'parameters_millions'), 2)} | {display(value(row, 'milliseconds_per_image'), 2)} |"
        )
    lines += ["", "## Evidence-Based Findings", ""]
    labels = {"psnr": "PSNR", "ssim": "SSIM", "mae": "MAE", "lpips": "LPIPS", "fid": "FID"}
    for metric, model in win.items():
        lines.append(f"- Best {labels[metric]}: **{model}**.")
    lines += [
        "- Metric disagreements are retained because pixel fidelity and perceptual realism measure different behavior.",
        "- The final model is treated as the selected solution only if it improves the fixed test split or produces better inspected failure cases.",
        "",
        "## Terrain Analysis",
        "",
        "| Model | Terrain | Count | PSNR | SSIM | MAE | LPIPS |",
        "|---|---|---:|---:|---:|---:|---:|",
    ]
    for row in terrain:
        lines.append(
            f"| {row['model']} | {row['terrain']} | {row['count']} | {display(value(row, 'psnr'), 3)} | "
            f"{display(value(row, 'ssim'))} | {display(value(row, 'mae'))} | {display(value(row, 'lpips'))} |"
        )
    lines += ["", "## Training Record", ""]
    for row in training:
        lines.append(
            f"- {row['model']}: {row['epochs_completed']} epochs, best epoch {row['best_epoch']}, "
            f"best validation L1 {display(row['best_val_l1'])}, {display(row['training_minutes'], 1)} minutes."
        )
    lines += ["", "## Failure Analysis", ""]
    if failure:
        for report in failure:
            lines.append(
                f"- {report['model']}: lowest-SSIM terrain was **{report['worst_terrain_by_ssim']}**. "
                "See its worst-case montage before proposing a cause."
            )
    else:
        lines.append("- Failure summaries unavailable; run Script 09.")
    lines += [
        "",
        "## Limitations and Next Step",
        "",
        "SAR-to-EO synthesis is one-to-many: plausible optical color or texture is not proof of the true surface state. Results are limited by the dataset's geographic/seasonal coverage, image-level normalization, finite training budget, and pretrained perceptual features learned from natural imagery.",
        "",
        "The next research step is a conditional or latent diffusion model initialized from the strongest paired GAN experiment. Diffusion offers better mode coverage and uncertainty sampling, but requires a larger compute budget and careful testing for color drift, slow sampling, and hallucinated details.",
        "",
        "## Generated Artifacts",
        "",
        "- `model_results_table.csv`: machine-readable result table",
        "- `results_tables.tex`: PDF-ready LaTeX tables",
        "- `training_curves.png`: common validation trajectory",
        "- `metric_comparison.png` and `qualitative_comparison.png`: generated by Script 08",
    ]
    (report_dir / "FINAL_EXPERIMENT_REPORT.md").write_text("\n".join(lines))


def main() -> None:
    args = arguments()
    out_dir = Path(args.out_dir)
    evaluation = out_dir / f"evaluation_{args.split}"
    comparison_path = evaluation / "model_comparison.csv"
    terrain_path = evaluation / "terrain_comparison.csv"
    if not comparison_path.exists():
        raise SystemExit(f"Missing {comparison_path}. Run 08_evaluate_all_models.py first.")
    results = read_csv(comparison_path)
    terrain = read_csv(terrain_path) if terrain_path.exists() else []
    training = collect_training(out_dir)
    eda_path = out_dir / "eda_stats.json"
    eda = json.loads(eda_path.read_text()) if eda_path.exists() else {}
    failure_path = out_dir / "ablation_failure_analysis" / "failure_summaries.json"
    failure = json.loads(failure_path.read_text()) if failure_path.exists() else []
    report_dir = Path(args.report_dir) if args.report_dir else out_dir / "final_report"
    report_dir.mkdir(parents=True, exist_ok=True)
    with (report_dir / "model_results_table.csv").open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(results[0]))
        writer.writeheader()
        writer.writerows(results)
    plot_training_curves(out_dir, report_dir / "training_curves.png")
    write_latex(results, training, report_dir / "results_tables.tex")
    write_markdown(results, terrain, training, eda, failure, report_dir)
    print("Final report artifacts:", report_dir)


if __name__ == "__main__":
    main()
